public class ShortOperatorsDemo {
    public static void main(String[] args) {
        int x;
        int y;
        x = 1;
        y = x++;
        System.out.println(x);
        System.out.println(y);
        System.out.println();
        x = 1;
        y = ++x;
        System.out.println(x);
        System.out.println(y);
        System.out.println();
        x = 10;
        y = 20;
        y += x;
        System.out.println(x);
        System.out.println(y);
        System.out.println();
        x = 10;
        y = 20;
        y /= x;
        System.out.println(x);
        System.out.println(y);
        System.out.println();
        x = 10;
        y = 20;
        y %= x;
        System.out.println(x);
        System.out.println(y);
        System.out.println();
        x = 10;
        y = 20;
        y *= x;
        System.out.println(x);
        System.out.println(y);

    }
}
